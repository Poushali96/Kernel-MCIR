# ============================================================

def kernel_mcir_scores_rff_gpu_final(X, y, out_dim=512, sigma=1.0):
    """
    FINAL correct MCIR-RFF implementation:
    - one global RFF for X
    - knockout-based feature importance
    - stable conditioning
    - GPU accelerated
    """

    # Send to GPU
    X_t = torch.tensor(X, dtype=torch.float32, device=device)
    y_t = torch.tensor(y.reshape(-1, 1), dtype=torch.float32, device=device)

    n, d = X.shape

    # ------------------------------
    # 1. Global RFF for X and y
    # ------------------------------
    Zx_global = rff_features_full(X_t, out_dim=out_dim, sigma=sigma)   # (n, m)
    Zy = rff_features_full(y_t, out_dim=out_dim, sigma=sigma)          # (n, m)

    scores = torch.zeros(d, device=device)

    # Conditioning storage
    conditioning = []    # each Δ_j is (n × m)

    for j in range(d):

        # ------------------------------
        # 2. Knockout feature j
        # ------------------------------
        X_removed = X_t.clone()
        X_removed[:, j] = 0.0

        Z_removed = rff_features_full(X_removed, out_dim=out_dim, sigma=sigma)

        # Δ_j = Z(X) - Z(X without feature j)
        Delta_j = Zx_global - Z_removed       # (n × m)

        # ------------------------------
        # Unique contribution U
        # ------------------------------
        U = torch.sum(Delta_j * Zy)
        U = torch.clamp(U, min=0)

        # ------------------------------
        # Redundant contribution R
        # ------------------------------
        if len(conditioning) > 0:
            Zstack = torch.cat(conditioning, dim=1)   # (n × (m*k))
            coef = torch.linalg.lstsq(Zstack, Delta_j).solution
            R = torch.sum((Zstack @ coef) * Zy)
            R = torch.clamp(R, min=0)
        else:
            R = torch.tensor(0.0, device=device)

        # ------------------------------
        # MCIR score
        # ------------------------------
        scores[j] = U / (U + R + 1e-12)

        # update conditioning
        conditioning.append(Delta_j)

    return scores.detach().cpu().numpy()

