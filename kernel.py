def rff_features(X, out_dim=512, sigma=1.0):
    """
    Random Fourier Features:
    Z(x) = sqrt(2/D) * cos(Wx + b)
    """
    if isinstance(X, np.ndarray):
        X = torch.tensor(X, dtype=torch.float32, device=device)

    n, d = X.shape
    W = torch.randn((d, out_dim), device=device) / sigma
    b = 2 * np.pi * torch.rand((1, out_dim), device=device)
    Z = torch.cos(X @ W + b)
    Z = torch.sqrt(torch.tensor(2.0/out_dim, device=device)) * Z
    return Z


# ------------------------------------------------------------
# 2. Kernel–MCIR (bounded [0,1], correct recurrence)
# ------------------------------------------------------------
def kernel_mcir_scores(X, y, out_dim=512):
    X_t = torch.tensor(X, dtype=torch.float32, device=device)
    y_t = torch.tensor(y.reshape(-1,1), dtype=torch.float32, device=device)

    Zy = rff_features(y_t, out_dim)
    m, d = X.shape

    scores = torch.zeros(d, device=device)
    conditioning = []

    for j in range(d):
        Zj = rff_features(X_t[:, j:j+1], out_dim)

        # Projection update
        phi_old = sum(conditioning) if conditioning else torch.zeros_like(Zy)
        phi_new = phi_old + (Zj - phi_old)

        # Unique contribution
        U = torch.sum((Zj - phi_old) * Zy)

        # Redundant contribution
        if conditioning:
            Zstack = torch.cat(conditioning, dim=1)
            coef = torch.linalg.lstsq(Zstack, Zj).solution
            R = torch.sum((Zstack @ coef) * Zy)
        else:
            R = torch.tensor(0., device=device)

        # Enforce non-negativity from projection geometry
        U = torch.clamp(U, min=0.)
        R = torch.clamp(R, min=0.)

        # MCIR score
        scores[j] = U / (U + R + 1e-12)

        conditioning.append(Zj)

    return scores.detach().cpu().numpy()


