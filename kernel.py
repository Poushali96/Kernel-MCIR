import numpy as np
from sklearn.metrics.pairwise import rbf_kernel, linear_kernel

def center_kernel(K):
    n = K.shape[0]
    H = np.eye(n) - np.ones((n, n)) / n
    return H @ K @ H

# ------------------------------------------------------------
# HSIC scores (per-feature)
# ------------------------------------------------------------
def hsic_scores(X, y, gamma=None):
    n, d = X.shape
    Ky = center_kernel(rbf_kernel(y.reshape(-1, 1), gamma=gamma))
    scores = np.zeros(d)

    for j in range(d):
        Kx = center_kernel(rbf_kernel(X[:, j].reshape(-1, 1), gamma=gamma))
        scores[j] = np.sum(Kx * Ky)
    return scores

# ------------------------------------------------------------
# CKA scores (per-feature)
# ------------------------------------------------------------
def cka_scores(X, y, gamma=None):
    n, d = X.shape
    Ky = center_kernel(rbf_kernel(y.reshape(-1, 1), gamma=gamma))
    scores = np.zeros(d)

    Ky_norm = np.sqrt(np.sum(Ky * Ky))

    for j in range(d):
        Kx = center_kernel(rbf_kernel(X[:, j].reshape(-1, 1), gamma=gamma))
        Kx_norm = np.sqrt(np.sum(Kx * Kx))
        scores[j] = np.sum(Kx * Ky) / (Kx_norm * Ky_norm + 1e-12)
    return scores

# ------------------------------------------------------------
# Kernel MI (simple density-ratio version)
# ------------------------------------------------------------
def kernel_mi_scores(X, y, gamma=None):
    n, d = X.shape
    Ky = center_kernel(rbf_kernel(y.reshape(-1, 1), gamma=gamma))
    scores = np.zeros(d)

    for j in range(d):
        Kx = center_kernel(rbf_kernel(X[:, j].reshape(-1, 1), gamma=gamma))
        scores[j] = np.trace(Kx @ Ky)
    return scores



