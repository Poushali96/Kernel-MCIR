def rff_features_full(X, out_dim=512, sigma=1.0):
    """
    X: (n, d)
    returns: Z (n, out_dim)
    Approximates full multivariate RBF kernel using Random Fourier Features.
    """
    n, d = X.shape

    # W: (d, out_dim)
    W = torch.randn((d, out_dim), device=device) / sigma

    # Random phase b: (1, out_dim)
    b = 2 * np.pi * torch.rand((1, out_dim), device=device)

    # z = sqrt(2/m) * cos(XW + b)
    Z = torch.sqrt(torch.tensor(2.0/out_dim, device=device)) * torch.cos(X @ W + b)
    return Z


