def integrated_gradients(model, inputs, target_idx):
    inputs = inputs.requires_grad_(True)
    baseline = torch.zeros_like(inputs)
    steps = 50
    scaled = [baseline + (float(i)/steps)*(inputs-baseline) for i in range(steps)]
    grads = []
    for x in scaled:
        out = model(x)
        out[0, target_idx].backward(retain_graph=True)
        grads.append(inputs.grad.detach().clone().cpu().numpy())
        inputs.grad.zero_()
    grads = np.array(grads).mean(axis=0)
    return grads

