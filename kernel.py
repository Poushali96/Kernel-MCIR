def rff_features_torch(x, out_dim=512):
    """
    x: (n,1) torch tensor
    returns Z: (n,out_dim)
    """
    n = x.shape[0]

    W = torch.randn((1, out_dim), device=device)  # Gaussian projection
    B = 2 * np.pi * torch.rand(out_dim, device=device)

    # RFF: z = sqrt(2/D) * cos(Wx + b)
    Z = torch.sqrt(torch.tensor(2.0/out_dim, device=device)) * torch.cos(x @ W + B)
    return Z


# ============================================================
# 2. GPU Projection Operation (Least Squares)
# ============================================================

def proj_Z_torch(targetZ, Z_list):
    """
    Projects targetZ onto span(Z_list)
    All ops on GPU
    """
    if len(Z_list) == 0:
        return torch.zeros_like(targetZ)

    Zstack = torch.cat(Z_list, dim=1)  # (n, r*k)
    coef = torch.linalg.lstsq(Zstack, targetZ).solution
    return Zstack @ coef


# ============================================================
# 3. Kernel–MCIR (FAST GPU RFF VERSION)
# ============================================================

def kernel_mcir_scores_rff_gpu(X, y, out_dim=512):
    n, d = X.shape

    X_t = torch.tensor(X, dtype=torch.float32, device=device)
    y_t = torch.tensor(y.reshape(-1,1), dtype=torch.float32, device=device)

    # Output RFF
    yZ = rff_features_torch(y_t, out_dim)

    # Per-feature RFFs
    Zs = [rff_features_torch(X_t[:, j:j+1], out_dim) for j in range(d)]

    scores = torch.zeros(d, device=device)
    conditioning = []

    for i in range(d):
        Zi = Zs[i]

        phi = proj_Z_torch(yZ, conditioning)
        phi_new = proj_Z_torch(yZ, conditioning + [Zi])
        Delta = phi_new - phi

        # Unique contribution
        U = torch.sum(Zi * Delta)

        # Redundant contribution
        if len(conditioning) == 0:
            R = torch.tensor(0.0, device=device)
        else:
            Zproj = proj_Z_torch(Zi, conditioning)
            R = torch.sum(Zproj * phi)

        scores[i] = U / (U + R + 1e-12)

        conditioning.append(Zi)

    return scores.detach().cpu().numpy()


