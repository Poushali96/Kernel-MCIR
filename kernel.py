def center_kernel(K):
    n = K.shape[0]
    H = np.eye(n) - np.ones((n,n))/n
    return H @ K @ H

def compute_rbf(X, sigma=None):
    D = squareform(pdist(X, 'euclidean'))
    if sigma is None:
        sigma = np.median(D[D > 0])
    K = rbf_kernel(X, gamma=1/(2*sigma**2))
    return center_kernel(K)

def projection_operator(K_list):
    if len(K_list) == 0:
        return None
    n = K_list[0].shape[0]
    A = np.stack([K.reshape(-1) for K in K_list], axis=1)
    P = A @ np.linalg.pinv(A.T @ A + 1e-8*np.eye(A.shape[1])) @ A.T
    return P.reshape(n*n, n*n)

def project(K, P):
    if P is None:
        return np.zeros_like(K)
    n = K.shape[0]
    return (P @ K.reshape(-1)).reshape(n,n)

def kernel_mcir(K_feats, K_out):
    d = len(K_feats)
    n = K_out.shape[0]

    K_Phi = []
    U = np.zeros(d)
    R = np.zeros(d)

    for i in range(d):
        fK = K_feats[i]

        # Projection with current Φ
        P_old = projection_operator(K_Phi)
        L_old = project(K_out, P_old)

        # Projection with Φ ∪ {i}
        K_new = K_Phi + [fK]
        P_new = projection_operator(K_new)
        L_new = project(K_out, P_new)

        Delta = L_new - L_old

        # Unique influence
        U[i] = np.trace(fK @ Delta)

        # Redundant influence
        fK_proj = project(fK, P_old)
        R[i] = np.trace(fK_proj @ L_old)

        K_Phi.append(fK)

    KMCIR = U / (U + R + 1e-12)
    return KMCIR, U, R

def hsic(K, L):
    n = K.shape[0]
    Kc = center_kernel(K)
    Lc = center_kernel(L)
    return np.trace(Kc @ Lc) / (n-1)**2

def cka(K, L):
    Kc = center_kernel(K)
    Lc = center_kernel(L)
    num = np.sum(Kc * Lc)
    den = np.sqrt(np.sum(Kc*Kc) * np.sum(Lc*Lc))
    return num / (den + 1e-12)

