# ------------------------------------------------------------
def unique_only(X, y, out_dim=512):
    X_t = torch.tensor(X, dtype=torch.float32, device=device)
    y_t = torch.tensor(y.reshape(-1,1), dtype=torch.float32, device=device)
    Zy = rff_features(y_t, out_dim)

    d = X.shape[1]
    out = np.zeros(d)
    for j in range(d):
        Zj = rff_features(X_t[:, j:j+1], out_dim)
        out[j] = torch.sum(Zj * Zy).item()
    return out


