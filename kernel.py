    grads = []

    for i in range(steps):
        alpha = float(i) / steps

        # scaled non-leaf tensor
        x_scaled = baseline + alpha * (x_img - baseline)
        x_scaled.requires_grad_(True)
        x_scaled.retain_grad()     # <-- IMPORTANT

        out = model(x_scaled)
        score = out[0, target_idx]

        score.backward(retain_graph=True)

        grads.append(x_scaled.grad.detach().cpu().numpy().copy())

        model.zero_grad(set_to_none=True)

    # Average gradients
    avg_grad = np.mean(np.array(grads), axis=0)

    # Convert to numpy safely
    x_np = x_img.detach().cpu().numpy()
    base_np = baseline.detach().cpu().numpy()

    ig = (x_np - base_np) * avg_grad
    return ig.flatten()

