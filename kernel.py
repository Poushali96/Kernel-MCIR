import numpy as np
from sklearn.metrics.pairwise import rbf_kernel, linear_kernel

# ------------------------------------------------------------
# Helper: center a kernel matrix
# ------------------------------------------------------------
def center_kernel(K):
    n = K.shape[0]
    H = np.eye(n) - np.ones((n, n)) / n
    return H @ K @ H

# ------------------------------------------------------------
# Helper: projection of A onto span(K_list)
# ------------------------------------------------------------
def project_onto_kernels(A, K_list, ridge=1e-6):
    """
    Compute projection of matrix A onto span of kernel matrices.
    Equivalent to solving regression in RKHS but implemented linearly.
    """
    if len(K_list) == 0:
        return np.zeros_like(A)

    Kstack = np.stack(K_list, axis=2)    # shape: n x n x p
    p = Kstack.shape[2]
    M = np.zeros_like(A)

    # Solve least-squares weights for projection
    for i in range(p):
        Ki = Kstack[:, :, i]
        # Compute coefficient via Frobenius least squares
        alpha = np.sum(A * Ki) / (np.sum(Ki * Ki) + ridge)
        M += alpha * Ki
    return M

# ------------------------------------------------------------
# Kernel–MCIR computation
# ------------------------------------------------------------
def kernel_mcir_scores(X, y, kernel="rbf", gamma=None):
    """
    Compute Kernel–MCIR scores for each feature in X.
    X : n x d
    y : n vector
    """
    n, d = X.shape

    # Output kernel
    if kernel == "rbf":
        Ky = rbf_kernel(y.reshape(-1, 1), gamma=gamma)
    else:
        Ky = linear_kernel(y.reshape(-1, 1))

    Ky = center_kernel(Ky)

    # Per-feature kernels
    K_list = []
    for j in range(d):
        if kernel == "rbf":
            Kj = rbf_kernel(X[:, j].reshape(-1, 1), gamma=gamma)
        else:
            Kj = linear_kernel(X[:, j].reshape(-1, 1))
        K_list.append(center_kernel(Kj))

    # Compute scores
    scores = np.zeros(d)
    conditioning = []

    for i in range(d):
        Ki = K_list[i]

        # Current projection and new projection
        L_phi = project_onto_kernels(Ky, conditioning)
        L_new = project_onto_kernels(Ky, conditioning + [Ki])

        Delta_i = L_new - L_phi

        # Unique contribution
        U = np.sum(Ki * Delta_i)

        # Redundant contribution
        R = np.sum(project_onto_kernels(Ki, conditioning) * L_phi)

        # Kernel–MCIR score
        scores[i] = U / (U + R + 1e-12)

    return scores

