# ----------------------------
# RFF (cosine) kernel
# ----------------------------

def rff_features(X, out_dim=600):
    X = torch.tensor(X, device=device, dtype=torch.float32)
    d = X.shape[1]
    W = torch.randn(d, out_dim, device=device)
    b = 2 * np.pi * torch.rand(out_dim, device=device)
    return torch.sqrt(torch.tensor(2.0/out_dim, device=device)) * torch.cos(X @ W + b)

# ----------------------------
# RKHS projection
# ----------------------------

def proj(Zy, Zlist):
    if len(Zlist) == 0:
        return torch.zeros_like(Zy)
    Zmat = torch.cat(Zlist, dim=1)
    coef = torch.linalg.lstsq(Zmat, Zy).solution
    return Zmat @ coef

# ----------------------------
# Kernel–MCIR (correct + bounded)
# ----------------------------

def kernel_mcir_gpu(X, y, out_dim=600):
    Z_y = rff_features(y.reshape(-1,1), out_dim)

    Z_feats = [rff_features(X[:,j:j+1], out_dim) for j in range(X.shape[1])]
    conditioning = []
    scores = []

    for Zi in Z_feats:
        phi = proj(Z_y, conditioning)
        phi_new = proj(Z_y, conditioning + [Zi])
        Delta = phi_new - phi

        U = torch.sum(Zi * Delta)

        if len(conditioning) == 0:
            R = torch.tensor(0.0, device=device)
        else:
            R = torch.sum(proj(Zi, conditioning) * phi)

        # ensure nonnegativity (projection geometry)
        U = torch.relu(U)
        R = torch.relu(R)

        score = (U / (U + R + 1e-12)).item()
        scores.append(score)

        conditioning.append(Zi)

    return np.array(scores)

