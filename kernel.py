def add_gaussian_noise(X, frac=0.2, sigma=0.3):
    Xn = X.copy()
    d = X.shape[1]
    idx = np.random.choice(d, int(frac * d), replace=False)
    Xn[:, idx] += sigma * np.random.randn(*Xn[:, idx].shape)
    return Xn


def add_adversarial_noise(X, y, eps=0.15):
    grads = np.sign(np.corrcoef(X.T, y)[-1, :-1])
    return X + eps * grads


